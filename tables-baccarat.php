					<h4>La grande table de baccarat</h4>
					<p>La grande table de baccarat est de forme ovale et le tapis est de couleur verte. Le tapis désigne les places des joueurs (généralement de 1 à 15, en passant la 13ème pour des raisons de superstition, dans certains endroits ce sera le numéro 4, car dans les cultures orientales le numéro 4 est considéré comme porte-malheur et est étroitement lié à la mort). Pour les joueurs asiatiques le baccarat est un jeu de choix. </p>
					<p>Sur un côté de la table il y a une place en forme de la lettre U pour marquer la place d'un des croupiers. Ce croupier est appelé « caller ». Il dirige le jeu, annonce la combinaison des cartes et appelle le vainqueur (d'où « caller », en anglais call – appeler). Le « caller » contrôle également le sabot qui se déplace d'un joueur à l'autre sur la table. Le sabot fait donc le tour de la table, en passant d'un joueur à l'autre. Il y a deux autres croupiers à la <strong>table de baccarat</strong> L'un sert les joueurs sur sa gauche, l'autre ceux sur sa droite. Ils distribuent donc les quatre cartes face cachée du sabot. Ils en distribuent deux à la maison (ou la banque) et deux au joueur. </p>
					<p>Les deux des quatre cartes distribuées et qui se trouvent en réalité en face du « caller » constituent la main du joueur, les deux autres qui se trouvent en dessous du sabot constituent la main du banquier/banque. Le croupier passe les deux cartes toujours face cachée au joueur qui a placé le plus grand pari sur le joueur et c'est à cette personne de retourner les cartes afin de révéler la main. Ensuite, ces deux cartes vont de nouveau au croupier. Le croupier, après avoir placé les cartes dans la case appropriée de la table pour 'la main du joueur', annonce le total. </p>
					<p>Il en va de même pour la main du banquier : les cartes sont placées dans la case 'la main du banquier'. Lorsque les deux mains sont révélées, il y a soit une décision finale soit l'égalité ou partie nulle.</p>
