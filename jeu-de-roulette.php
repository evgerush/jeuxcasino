					<p>Maintenant regardons les différents types de mises possibles à la roulette. Un pari à cheval, ou sur deux numéros paie 17 :1. Pour placer le pari sur les deux numéros, le joueur doit mettre ses jetons sur la ligne qui sépare les deux numéros. Un pari sur la transversale, ou sur trois numéros peut être fait en plaçant par exemple les jetons sur le 0, 00 et le 2 (ces numéros sont interconnectés) ou sur n'importe quels autres numéros interconnectés. Un tel pari gagnant paie 11 :1. Une mise sur quatre numéros ou le carré doit être placée sur l'intersection des quatre numéros. Le carré paie 8 :1. Une mise sur cinq numéros, ou les cinq premiers doit couvrir le 0, le 00, le 1, le 2 et le 3. Une telle mise rapporte 6 :1, mais comme c'est la seule mise qui augmente l'avantage de la maison jusqu'à 8%, elle n'est pas du tout recommandée. </p>
